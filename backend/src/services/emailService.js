/**
 * Email Service for Scheduled Reports
 * Provides email delivery functionality for automated report distribution
 */

import nodemailer from 'nodemailer';
import path from 'path';

/**
 * Create email transporter based on environment configuration
 */
function createTransporter() {
  // Production: Use SMTP configuration from environment
  if (process.env.NODE_ENV === 'production' || process.env.SMTP_HOST) {
    return nodemailer.createTransporter({
      host: process.env.SMTP_HOST,
      port: parseInt(process.env.SMTP_PORT || '587'),
      secure: process.env.SMTP_SECURE === 'true', // true for 465, false for other ports
      auth: {
        user: process.env.SMTP_USER,
        pass: process.env.SMTP_PASSWORD
      },
      from: process.env.SMTP_FROM || 'noreply@shotspot.app'
    });
  }
  
  // Development/Test: Use ethereal.email test account
  // Note: In production, you should configure real SMTP settings
  if (process.env.NODE_ENV === 'development') {
    console.log('‚ö†Ô∏è  Using test email account. Configure SMTP_* environment variables for production.');
  }
  
  // Return null transporter for test environment to prevent actual emails
  return null;
}

