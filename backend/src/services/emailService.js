/**
 * Generate plain text version of report email
 */
function generateReportEmailText(options) {
  const {
    reportName,
    reportType,
    teamName,
    generatedBy,
    downloadUrl,
    expiresAt
  } = options;

  let text = 'ShotSpot Report Ready\n\n';
  text += 'Your scheduled report has been generated and is ready for download.\n\n';
  text += 'Report Details:\n';
  text += `- Report Name: ${reportName}\n`;
  text += `- Report Type: ${reportType}\n`;
  if (teamName) text += `- Team: ${teamName}\n`;
  text += `- Generated By: ${generatedBy}\n`;
  text += `- Generated At: ${new Date().toLocaleString()}\n\n`;
  
  if (downloadUrl) {
    text += `Download Link: ${downloadUrl}\n\n`;
  }
  
  if (expiresAt) {
    text += `‚ö†Ô∏è Note: This report will be automatically deleted on ${new Date(expiresAt).toLocaleDateString()}.\n`;
    text += 'Please download it before then if you need to keep a copy.\n\n';
  }
  
  text += '---\n';
  text += 'This is an automated message from ShotSpot - Korfball Statistics Platform\n';
  
  return text;
}

/**
 * Send report email to recipients
 * 
 * @param {Object} options - Email options
 * @param {string[]} options.recipients - Array of email addresses
 * @param {string} options.subject - Email subject
 * @param {string} options.reportName - Name of the report
 * @param {string} options.reportType - Type of report (game, team, season, etc.)
 * @param {string} [options.teamName] - Team name if applicable
 * @param {string} options.generatedBy - Username of person who created the schedule
 * @param {string} [options.downloadUrl] - Full URL to download the report
 * @param {string} [options.filePath] - Path to attachment file
 * @param {Date} [options.expiresAt] - When the report expires
 * @returns {Promise<Object>} - Result of email sending
 */
export async function sendReportEmail(options) {
  const {
    recipients,
    subject,
    reportName,
    reportType,
    teamName,
    generatedBy,
    downloadUrl,
    filePath,
    expiresAt
  } = options;

  // Validate recipients
  if (!recipients || !Array.isArray(recipients) || recipients.length === 0) {
    throw new Error('At least one recipient email address is required');
  }

  // Don't actually send emails in test environment
  if (process.env.NODE_ENV === 'test') {
    if (process.env.NODE_ENV !== 'test') {
      console.log('üìß [TEST MODE] Would send email:', {
        to: recipients,
        subject,
        reportName
      });
    }
    return {
      success: true,
      messageId: 'test-message-id',
      recipients,
      testMode: true
    };
  }

  const transporter = createTransporter();
  
  if (!transporter) {
    if (process.env.NODE_ENV !== 'test') {
      console.warn('‚ö†Ô∏è  Email transporter not configured. Skipping email send.');
    }
    return {
      success: false,
      error: 'Email service not configured',
      recipients
    };
  }

  // Generate email content
  const html = generateReportEmailHTML({
    reportName,
    reportType,
    teamName,
    generatedBy,
    downloadUrl,
    expiresAt
  });

  const text = generateReportEmailText({
    reportName,
    reportType,
    teamName,
    generatedBy,
    downloadUrl,
    expiresAt
  });

  // Prepare email options
  const mailOptions = {
    from: process.env.SMTP_FROM || 'noreply@shotspot.app',
    to: recipients.join(', '),
    subject: subject || `ShotSpot Report: ${reportName}`,
    text,
    html
  };

  // Attach file if provided
  if (filePath) {
    mailOptions.attachments = [
      {
        filename: path.basename(filePath),
        path: filePath
      }
    ];
  }

  try {
    const info = await transporter.sendMail(mailOptions);
    
    if (process.env.NODE_ENV !== 'test') {
      console.log('‚úÖ Email sent successfully:', info.messageId);
      if (process.env.NODE_ENV === 'development' && info.preview) {
        console.log('üìß Preview URL:', nodemailer.getTestMessageUrl(info));
      }
    }

    return {
      success: true,
      messageId: info.messageId,
      recipients,
      previewUrl: process.env.NODE_ENV === 'development' ? nodemailer.getTestMessageUrl(info) : null
    };
  } catch (error) {
    console.error('‚ùå Failed to send email:', error);
    return {
      success: false,
      error: error.message,
      recipients
    };
  }
}

/**
 * Verify email configuration
 * Useful for checking SMTP settings on startup
 */
export async function verifyEmailConfig() {
  if (process.env.NODE_ENV === 'test') {
    return { verified: true, message: 'Test environment - email verification skipped' };
  }

  const transporter = createTransporter();
  
  if (!transporter) {
    return {
      verified: false,
      message: 'Email service not configured. Set SMTP_* environment variables.'
    };
  }

  try {
    await transporter.verify();
    return {
      verified: true,
      message: 'Email configuration verified successfully'
    };
  } catch (error) {
    return {
      verified: false,
      message: `Email configuration error: ${error.message}`
    };
  }
}

export default {
  sendReportEmail,
  verifyEmailConfig
};
